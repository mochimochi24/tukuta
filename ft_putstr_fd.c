#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int		i;
	char	str;

	i = 0;
	while (s[i])
	{
		str = s[i];
		write(fd, &str, 1);
		i++;
	}
	return (0);
}
